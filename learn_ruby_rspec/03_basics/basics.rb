def who_is_bigger(a,b,c)
	tab = [a,b,c]
	if tab.include?nil
		"nil detected"

	else 
	tab.max 

		if tab.max == a
			"a is bigger"

		elsif tab.max == b
			"b is bigger"

		else 
			"c who_is_bigger bigger"
		end
	end
	
end

